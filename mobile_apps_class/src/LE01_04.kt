/*Escreva um algoritmo que leia dois números e ao final mostre a soma, subtração,
multiplicação e a divisão dos números lidos. */

fun operacoes(A : Int, B : Int)
{
    println("$A - $B = ${A - B}")
    println("$A + $B = ${A + B}")
    println("$A * $B = ${A * B}")
    println("$A / $B = ${A / B}")
}